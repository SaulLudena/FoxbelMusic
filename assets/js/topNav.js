
//funcion que permite abrir la barra de navegación lateral
function abrirNav(){
    //cambiando estilos de la barra de navegacion
    document.getElementById("nav").style.display="block";
}
//funcion que permite cerrar la barra de navegación lateral
function cerrarNav(){
    //cambiando estilos de la barra de navegacion
    document.getElementById("nav").style.display="none";
}